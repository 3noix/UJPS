RemoteJoystickClient::RemoteJoystickClient(QObject *parent) : AbstractRemoteJoystickClient(parent)
{
	
}

// DESCRIPTION ////////////////////////////////////////////////////////////////
QString RemoteJoystickClient::description() const {return "TouchScreen2";}

// BUTTON COUNT ///////////////////////////////////////////////////////////////
quint8 RemoteJoystickClient::buttonCount() const {return 2;}

// BUTTONS NAMES //////////////////////////////////////////////////////////////
QStringList RemoteJoystickClient::buttonsNames() const {return {"B1","B2"};}

// AXIS COUNT /////////////////////////////////////////////////////////////////
quint8 RemoteJoystickClient::axisCount() const {return 1;}

// AXES NAMES /////////////////////////////////////////////////////////////////
QStringList RemoteJoystickClient::axesNames() const {return {"A1"};}

// SET DATA ///////////////////////////////////////////////////////////////////
void RemoteJoystickClient::setData(const QString &prop, QVariant v)
{
	emit signalSetData(prop,v);
}

